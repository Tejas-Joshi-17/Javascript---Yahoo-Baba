// 1) ceil(x) :- Return Ceiling Value of x

console.log(`Ceiling Value of 5.2 is :- ${Math.ceil(5.2)}`)       // Ceiling Value of 5.2 is :- 6
console.log(`Ceiling Value of 4.9 is :- ${Math.ceil(4.9)}`)       // Ceiling Value of 4.9 is :- 5
console.log(`Ceiling Value of 3.5 is :- ${Math.ceil(3.5)}`)       // Ceiling Value of 3.5 is :- 4
console.log(`Ceiling Value of -3.5 is :- ${Math.ceil(-3.5)}`)     // Ceiling Value of -3.5 is :- -3
console.log(`Ceiling Value of -1.90 is :- ${Math.ceil(-1.90)}`)   // Ceiling Value of -1.90 is :- -1

// ------------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 2) floor(x) :- Return Flor Value of x

console.log(`Floor Value of 5.2 is :- ${Math.floor(5.2)}`)       // Floor Value of 5.2 is :- 5
console.log(`Floor Value of 4.9 is :- ${Math.floor(4.9)}`)       // Floor Value of 4.9 is :- 4
console.log(`Floor Value of 3.5 is :- ${Math.floor(3.5)}`)       // Floor Value of 3.5 is :- 3
console.log(`Floor Value of -3.5 is :- ${Math.floor(-3.5)}`)     // Floor Value of -3.5 is :- -4
console.log(`Floor Value of -1.90 is :- ${Math.floor(-1.90)}`)   // Floor Value of -1.90 is :- -2

// ------------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 3) round(x) :- Return Round-Off Value
console.log(`Round-Off Value of 5.2 is :- ${Math.round(5.2)}`)       // Round-Off Value of 5.2 is :- 5
console.log(`Round-Off Value of 4.9 is :- ${Math.round(4.9)}`)       // Round-Off Value of 4.9 is :- 5
console.log(`Round-Off Value of 3.5 is :- ${Math.round(3.5)}`)       // Round-Off Value of 3.5 is :- 4
console.log(`Round-Off Value of -3.5 is :- ${Math.round(-3.5)}`)     // Round-Off Value of -3.5 is :- -3
console.log(`Round-Off Value of -1.90 is :- ${Math.round(-1.90)}`)   // Round-Off Value of -1.90 is :- -2

// ------------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 4) trunc(x) :- Only Return Integer Value
console.log(`Integer Value of 5.2 is :- ${Math.trunc(5.2)}`)           // Integer Value of 5.2 is :- 5
console.log(`Integer Value of -1.90 is :- ${Math.trunc(-1.90)}`)       // Integer Value of 4.9 is :- -1
console.log(`Integer Value of 100.01 is :- ${Math.trunc(100.01)}`)     // Integer Value of 3.5 is :- 100

// ------------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 5) max(a,b,c,..) :- Return Maximum among all values
console.log(`Maximum in 2,80,456,100,89 is :- ${Math.max(2,80,456,100,89)}`)  // Maximum in 2,80,456,100,89 is :- 456

// ------------------------------------------------------------------------------------------------------------------------------------------------------------------------

// 6) min(a,b,c,..) :- Return Minimum among all values
console.log(`Minimum in 2,80,456,100,89 is :- ${Math.min(2,80,456,100,89)}`)  // Minimum in 2,80,456,100,89 is :- 2

