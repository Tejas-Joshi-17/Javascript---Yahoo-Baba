// If-Else Statements

const a = 10;
const b = 20;


if(a<15 && b===20){
    console.log(a + " less than 15 & " + b + " equals to 20")
}else{
    console.log("Ended")
}
// 10 less than 15 & 20 equals to 20



if(a<15 && b!==20){
    console.log(a + " less than 15 & " + b + " equals to 20")
}else{
    console.log("Ended")
}
// Ended


