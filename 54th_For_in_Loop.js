// For-In Loop :- Used to Traverse Object 

let Obj_1 = {
    Name : "Tejas Joshi",
    Age : 21,
    CGPA : 9.16,
}

for(let index in Obj_1){
    console.log(`${index} -> ${Obj_1[index]}`)
}
// Name -> Tejas Joshi
// Age -> 21
// CGPA -> 9.16

