// 1) Number()  :- Used to Convert into Number

let str1 = `99`;
let str2 = true;
let str3 = false;
console.log(`Number of ${str1} is :- ${Number(str1)}`)      // Number of 99 is :- 99
console.log(`Number of ${str2} is :- ${Number(str2)}`)      // Number of true is :- 1
console.log(`Number of ${str3} is :- ${Number(str3)}`)      // Number of false is :- 0

